#include "seg.h"

unsigned char seg_dula[] = {0xc0,0xf9,0xa4,0xb0,0x99,0x92,0x82,0xf8,0x80,0x90,0xbf,0xff};
unsigned char seg_wela[] = {0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};

void Seg_Disp(unsigned char dula,wela,point,star,star_flag)
{
	P0 = 0xff;
	P2 = P2 & 0x1f | 0xe0;
	P2 &= 0x1f;
	
	if((star & seg_wela[wela]) && (star_flag == 1))
		P0 = 0;
	else
		P0 = seg_wela[wela];
	P2 = P2 & 0x1f | 0xc0;
	P2 &= 0x1f;

	if(point)
		P0 = seg_dula[dula] & 0x7f;
	else
		P0 = seg_dula[dula];
	P2 = P2 & 0x1f | 0xe0;
	P2 &= 0x1f;	
}
#include <Seg.H>

unsigned char Seg_wela[8] = {0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};
unsigned char Seg_dula[] = {0xC0,0xF9,0xA4,0xB0,0x99,0x92,0x82,0xF8,0x80,0x90,0xff,0xbf,};

void Seg_Disp(unsigned char wela,dula)
{
	P0 = 0xff;
	P2 = P2 & 0x1f | 0xe0;
	P2 &=  0x1f;
	
	P0 = Seg_wela[wela];
	P2 = P2 & 0x1f | 0xc0;
	P2 &=  0x1f;

	P0 = Seg_dula[dula];
	P2 = P2 & 0x1f | 0xe0;
	P2 &=  0x1f;	
}
#include <Seg.h>
unsigned char Seg_Wela[]={0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};
unsigned char Seg_Dula[]={0xc0,0xf9,0xa4,0xb0,0x99,0x92,0x82,0xf8,0x80,0x90,0xff,0xbf};
void Seg_Disp(unsigned char wela,dula)
{
	P0=0xff;
	P2=P2&0x1f|0xe0;
	P2&=0x1f;
	P0=Seg_Wela[wela];
	P2=P2&0x1f|0xc0;
	P2&=0x1f;
	P0=Seg_Dula[dula];
	P2=P2&0x1f|0xe0;
	P2&=0x1f;
}